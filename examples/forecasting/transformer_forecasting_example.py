    # 3. Initialize the model
    model = Transformer(
        n_steps=n_steps,
        n_features=n_features,
        n_pred_steps=n_pred_steps,
        n_pred_features=n_features,
        n_encoder_layers=1,
        n_decoder_layers=1,
        d_model=32,
        n_heads=2,
        d_k=16,
        d_v=16,
        d_ffn=32,
        dropout=0.1,
        epochs=2,
        device="cpu",
    )

    # 4. Train the model
    print("🚀 Training the Transformer forecasting model...")
    model.fit(train_set, val_set)

    # 5. Forecast
